# list datastructure
# List are mutable(changeable)
# list are ordered
# list have indexes

fruits=['apple','banana','mango','pineapple','orange','strawberry']
print(fruits)
fruits[2]="watermelon"
print(f"I love eating {fruits[2]}")
numbers=[1,2,3,4,5,6,7,8,]
numbers.append(9)
print(numbers[4:])
# tuple datastructures
# tuples are immutable(unchangeable)
# tuples are ordered
# tuples have indexes
cars =('BMW','Audi','Subaru','Mercedes','Toyota','Nissan')
print(cars[3])
nambari=(1,2,3,4,5,6,7,8,9)
print(nambari)

# set datastructures
# set are non-ordered
# set do not have indexes
days={'Monday','Tuesday','Wednesday','Thursday','Friday','Saturday','Sunday'}
print(days)

names=['Lulu','Chris','Floridah']
print(names)
# dictionaries datastructures
students = {"Name":"Adorah","Age":17,"Gender":"f","School":"eMobilis"}
print(students)
print(students["Name"])
print(students["Age"])